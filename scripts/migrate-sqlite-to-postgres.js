/* eslint-disable no-console */
const fs = require('fs');
const path = require('path');
const Database = require('better-sqlite3');
const { Pool } = require('pg');

function quoteIdent(name) {
  return `"${String(name).replace(/"/g, '""')}"`;
}

function mapSqliteTypeToPostgres(typeRaw, colName, isPrimaryKey) {
  const type = String(typeRaw || '').toUpperCase();

  if (isPrimaryKey && colName === 'id' && type.includes('INT')) {
    return 'BIGINT GENERATED BY DEFAULT AS IDENTITY';
  }

  if (type.includes('INT')) return 'BIGINT';
  if (type.includes('REAL') || type.includes('FLOA') || type.includes('DOUB')) return 'DOUBLE PRECISION';
  if (type.includes('BLOB')) return 'BYTEA';
  if (type.includes('NUM') || type.includes('DEC')) return 'NUMERIC';
  if (type.includes('BOOL')) return 'BOOLEAN';
  if (type.includes('DATE') || type.includes('TIME')) return 'TIMESTAMPTZ';
  return 'TEXT';
}

function normalizeDefault(defaultValue) {
  if (defaultValue === null || defaultValue === undefined) return null;

  const raw = String(defaultValue).trim();
  if (!raw) return null;

  if (/^\(?datetime\('now'\)\)?$/i.test(raw)) return 'CURRENT_TIMESTAMP';
  if (/^\(?current_timestamp\)?$/i.test(raw)) return 'CURRENT_TIMESTAMP';

  return raw;
}

function isUuid(value) {
  if (typeof value !== 'string') return false;
  return /^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i.test(value);
}

function transformRow(tableName, row) {
  // Faithful LEGACY mirror: keep every SQLite row exactly as-is (int PKs,
  // password column, lead_id links, original status/type values). The shared
  // Postgres DB mirrors the SQLite schema 1:1, so no column/value remapping.
  return row;
}

async function ensureTable(client, sqlite, tableName) {
  const cols = sqlite.prepare(`PRAGMA table_info(${quoteIdent(tableName)})`).all();
  if (!cols.length) return;

  const pkCols = cols.filter((c) => Number(c.pk) > 0).sort((a, b) => Number(a.pk) - Number(b.pk));

  const colDefs = cols.map((col) => {
    const isPk = Number(col.pk) > 0;
    const pgType = mapSqliteTypeToPostgres(col.type, col.name, isPk && pkCols.length === 1);
    const parts = [`${quoteIdent(col.name)} ${pgType}`];

    if (!isPk && Number(col.notnull) === 1) {
      parts.push('NOT NULL');
    }

    const normalizedDefault = normalizeDefault(col.dflt_value);
    if (normalizedDefault && !isPk) {
      parts.push(`DEFAULT ${normalizedDefault}`);
    }

    if (isPk && pkCols.length === 1) {
      parts.push('PRIMARY KEY');
    }

    return parts.join(' ');
  });

  if (pkCols.length > 1) {
    const composite = pkCols.map((c) => quoteIdent(c.name)).join(', ');
    colDefs.push(`PRIMARY KEY (${composite})`);
  }

  const createSql = `CREATE TABLE IF NOT EXISTS ${quoteIdent(tableName)} (${colDefs.join(', ')})`;
  await client.query(createSql);
}

async function ensureIndexes(client, sqlite, tableName) {
  const indexList = sqlite.prepare(`PRAGMA index_list(${quoteIdent(tableName)})`).all();
  for (const idx of indexList) {
    const indexName = idx.name;
    if (!indexName || String(indexName).startsWith('sqlite_autoindex_')) continue;

    const indexCols = sqlite.prepare(`PRAGMA index_info(${quoteIdent(indexName)})`).all();
    if (!indexCols.length) continue;

    const colsSql = indexCols
      .sort((a, b) => Number(a.seqno) - Number(b.seqno))
      .map((c) => quoteIdent(c.name))
      .join(', ');

    const unique = Number(idx.unique) === 1 ? 'UNIQUE ' : '';
    const createIndexSql = `CREATE ${unique}INDEX IF NOT EXISTS ${quoteIdent(indexName)} ON ${quoteIdent(tableName)} (${colsSql})`;
    await client.query(createIndexSql);
  }
}

async function migrateTable(client, sqlite, tableName) {
  const rows = sqlite.prepare(`SELECT * FROM ${quoteIdent(tableName)}`).all();
  await client.query(`DELETE FROM ${quoteIdent(tableName)}`);

  if (rows.length === 0) {
    console.log(`- ${tableName}: 0 rows`);
    return;
  }

  const targetColumns = await client
    .query(
      `SELECT column_name, data_type FROM information_schema.columns WHERE table_schema = 'public' AND table_name = $1`,
      [tableName]
    )
    .then((r) => r.rows);

  const targetColumnNames = targetColumns.map((row) => row.column_name);
  const targetColumnTypes = new Map(targetColumns.map((row) => [row.column_name, row.data_type]));

  const sampleRow = transformRow(tableName, rows[0]);
  let columns = Object.keys(sampleRow).filter((c) => targetColumnNames.includes(c));
  if (
    columns.includes('id') &&
    targetColumnTypes.get('id') === 'uuid' &&
    rows.some((r) => r.id !== null && r.id !== undefined && !isUuid(String(r.id)))
  ) {
    // Keep migration moving when SQLite integer PK meets PostgreSQL UUID PK.
    columns = columns.filter((c) => c !== 'id');
  }
  if (columns.length === 0) {
    console.log(`- ${tableName}: 0 rows (no common columns)`);
    return;
  }

  const colSql = columns.map(quoteIdent).join(', ');
  const placeholders = columns.map((_, i) => `$${i + 1}`).join(', ');
  const insertSql = `INSERT INTO ${quoteIdent(tableName)} (${colSql}) VALUES (${placeholders})`;

  let inserted = 0;
  let skipped = 0;
  for (const row of rows) {
    const transformed = transformRow(tableName, row);
    const values = columns.map((key) => {
      const value = transformed[key];
      if (typeof value === 'bigint') return Number(value);
      if (targetColumnTypes.get(key) === 'uuid' && value !== null && value !== undefined && !isUuid(String(value))) {
        return null;
      }
      return value;
    });
    try {
      await client.query(insertSql, values);
      inserted += 1;
    } catch {
      skipped += 1;
    }
  }

  if (columns.includes('id')) {
    const seqResetSql = `
      SELECT setval(
        pg_get_serial_sequence('${tableName}', 'id'),
        COALESCE((SELECT MAX(id) FROM ${quoteIdent(tableName)}), 1),
        true
      )
    `;
    try {
      await client.query(seqResetSql);
    } catch {
      // Non-identity PK or missing sequence; safe to skip.
    }
  }

  console.log(`- ${tableName}: ${inserted} inserted, ${skipped} skipped (source ${rows.length})`);
}

async function main() {
  const databaseUrl = process.env.DATABASE_URL;
  if (!databaseUrl || !/^postgres(ql)?:\/\//i.test(databaseUrl)) {
    throw new Error('Set DATABASE_URL to a valid postgres:// or postgresql:// URL before running migration.');
  }

  const sqlitePath = process.env.SQLITE_PATH
    ? path.resolve(process.env.SQLITE_PATH)
    : path.join(process.cwd(), 'data', (process.env.CRM_DB_FILE || 'splendid-crm.db').trim());

  if (!fs.existsSync(sqlitePath)) {
    throw new Error(`SQLite file not found: ${sqlitePath}`);
  }

  console.log(`SQLite source: ${sqlitePath}`);
  const sqlite = new Database(sqlitePath, { readonly: true });

  const pool = new Pool({ connectionString: databaseUrl });
  const client = await pool.connect();

  try {
    const tables = sqlite
      .prepare("SELECT name FROM sqlite_master WHERE type = 'table' AND name NOT LIKE 'sqlite_%' ORDER BY name")
      .all()
      .map((r) => r.name)
      .filter(Boolean);

    if (!tables.length) {
      console.log('No tables found in SQLite source.');
      return;
    }

    for (const tableName of tables) {
      await ensureTable(client, sqlite, tableName);
    }

    for (const tableName of tables) {
      await migrateTable(client, sqlite, tableName);
    }

    for (const tableName of tables) {
      try {
        await ensureIndexes(client, sqlite, tableName);
      } catch {
        // Existing managed schema may be owned by another role; data migration can continue.
      }
    }
    console.log('Migration completed successfully.');
  } catch (err) {
    throw err;
  } finally {
    client.release();
    await pool.end();
    sqlite.close();
  }
}

main().catch((err) => {
  console.error('Migration failed:', err.message || err);
  process.exit(1);
});
